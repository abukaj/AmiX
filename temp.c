int ncsstrcmp(char *cs, char *ct){
  while (*cs != '\0' && tolower(*cs) == tolower(*ct)){
    cs++; ct++;
    }
  return *cs - *ct;
  }

/*****************************************************************************\
\*****************************************************************************/
int ncsstrncmp(char *cs, char *ct, int n){
  if (n == 0)
    return 0;
  while (*cs != '\0' && tolower(*cs) == tolower(*ct) && --n > 0){
    cs++; ct++;
    }
  return *cs - *ct;
  }
